#pragma once
#include "character/characterController.h"
class Character :public IGameObject
{
public:
	~Character() {
		DeleteGO(m_Model);
	};


	//初期位置を設定
	void SetSpownPos(const CVector3& pos) {
		m_SpownPosition = pos;
	}

	//モデルの初期化
	void CharacterInit(wchar_t* filePath, float radius, float hight, const CVector3& pos);

	//回転処理を各自設定
	virtual void Rotate() = 0;

	//回転量を設定
	void SetRotation(const CQuaternion& rot) {
		m_ModelRot = rot;
	}

	//位置を取得
	const CVector3& GetPosition() {
		return m_ModelPos;
	}

	//ここで動く
	void Move(CVector3& move);

	//モデルを更新する
	void CharacterModelUpdate();

	//前方向の更新をする
	virtual void ForwardUpdate();

	//移動量を取得
	CVector3 GetMoveSpeed()const {
		return m_MoveSpeed;
	}
protected:
	SkinModelRender* m_Model = nullptr;			//敵のモデル
	CVector3 m_ModelPos = CVector3::Zero();			//敵の位置
	CQuaternion m_ModelRot = CQuaternion::Identity();//敵の回転
	CVector3 m_ModelScale = CVector3::One();	//敵の大きさ
	CharacterController m_CharaCon;				//キャラコン

	CVector3 m_MoveSpeed = CVector3::Zero();	//移動量
	CVector3 m_forward = CVector3::Front();		//前方向
	CVector3 m_SpownPosition = CVector3::Zero();//初期位置
};

